class TextTerminal:
    """Character-mode display terminal backed by the shared vmem framebuffer."""

    VRAM_W = 243
    VRAM_H = 192
    CHAR_W = 8
    CHAR_H = 8
    COLS   = VRAM_W // CHAR_W   # 30 columns
    ROWS   = VRAM_H // CHAR_H   # 24 rows
    FG     =  121   # white  (display maps  121 → 255)
    BG     = -121   # black  (display maps -121 →   0)

    def __init__(self, vmem) -> None:
        self.vmem     = vmem
        self.cx       = 0
        self.cy       = 0
        self._cleared = False

    def write_char(self, ch: int) -> None:
        if not self._cleared:
            self._clear_screen()
        if ch == 10 or ch == 13:
            self.cx = 0
            self.cy += 1
            if self.cy >= self.ROWS:
                self._scroll()
        elif ch == 8 or ch == 127:
            if self.cx > 0:
                self.cx -= 1
                self._draw_char(32, self.cx, self.cy)
        elif 32 <= ch < 128:
            self._draw_char(ch, self.cx, self.cy)
            self.cx += 1
            if self.cx >= self.COLS:
                self.cx = 0
                self.cy += 1
                if self.cy >= self.ROWS:
                    self._scroll()

    def _clear_screen(self) -> None:
        raw = self.vmem._raw
        bg  = self.BG
        with self.vmem.lock:
            for i in range(self.VRAM_W * self.VRAM_H):
                raw[i] = bg
        self._cleared = True

    def _scroll(self) -> None:
        raw      = self.vmem._raw
        pitch    = self.VRAM_W
        row_px   = self.CHAR_H * pitch
        body_px  = (self.ROWS - 1) * row_px
        last     = (self.ROWS - 1) * row_px
        bg       = self.BG
        with self.vmem.lock:
            chunk = list(raw[row_px : row_px + body_px])
            raw[0 : body_px] = chunk
            raw[last : last + row_px] = [bg] * row_px
        self.cy = self.ROWS - 1

    def _draw_char(self, ch: int, col: int, row: int) -> None:
        raw   = self.vmem._raw
        pitch = self.VRAM_W
        px    = col * self.CHAR_W
        py    = row * self.CHAR_H
        fg    = self.FG
        bg    = self.BG
        base  = ch * 8
        size  = len(raw)
        with self.vmem.lock:
            for r in range(8):
                bits     = _FONT[base + r]
                row_base = (py + r) * pitch + px
                for c in range(8):
                    addr = row_base + c
                    if addr < size:
                        raw[addr] = fg if (bits >> (7 - c)) & 1 else bg
